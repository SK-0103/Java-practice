public class loops {
    public static void main(String[] args) {
        
        //while loop
        int count = 1;
        while(count !=5){
            System.out.println(count);
            count++;
        }

        //if statement
        int a = 11;
        if(a==10){
            System.out.println("a = 10");
        }
        else{
            System.out.println("A not equal to 10");
        }

        for (int c=0; c!=5; c++){
            System.out.println(c);
        }
    }
}
